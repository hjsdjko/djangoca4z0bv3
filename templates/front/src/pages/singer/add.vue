  export default {
    data() {
	  let self = this
      return {
        id: '',
        baseUrl: '',
        ro:{
				singer : false,
				biaoqian : false,
				touxiang : false,
				zimu : false,
				fengge : false,
				geshouxingbie : false,
				nianling : false,
				xingzuo : false,
				chushengriqi : false,
				diqu : false,
				gerenzhuanji : false,
				geshoujieshao : false,
				discussnum : false,
				storeupnum : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          singer: '',
          biaoqian: '',
          touxiang: '',
          zimu: '',
          fengge: '',
          geshouxingbie: '',
          nianling: '',
          xingzuo: '',
          chushengriqi: '',
          diqu: '',
          gerenzhuanji: '',
          geshoujieshao: '',
          discussnum: '',
          storeupnum: '',
        },
        biaoqianOptions: [],
        zimuOptions: [],
        geshouxingbieOptions: [],


        rules: {
          singer: [
            { required: true, message: '歌手姓名不能为空', trigger: 'blur' },
          ],
          biaoqian: [
            { required: true, message: '标签不能为空', trigger: 'blur' },
          ],
          touxiang: [
          ],
          zimu: [
          ],
          fengge: [
          ],
          geshouxingbie: [
          ],
          nianling: [
          ],
          xingzuo: [
          ],
          chushengriqi: [
          ],
          diqu: [
            { required: true, message: '地区不能为空', trigger: 'blur' },
          ],
          gerenzhuanji: [
          ],
          geshoujieshao: [
          ],
          discussnum: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          storeupnum: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='singer'){
              this.ruleForm.singer = obj[o];
              this.ro.singer = true;
              continue;
            }
            if(o=='biaoqian'){
              this.ruleForm.biaoqian = obj[o];
              this.ro.biaoqian = true;
              continue;
            }
            if(o=='touxiang'){
              this.ruleForm.touxiang = obj[o].split(",")[0];
              this.ro.touxiang = true;
              continue;
            }
            if(o=='zimu'){
              this.ruleForm.zimu = obj[o];
              this.ro.zimu = true;
              continue;
            }
            if(o=='fengge'){
              this.ruleForm.fengge = obj[o];
              this.ro.fengge = true;
              continue;
            }
            if(o=='geshouxingbie'){
              this.ruleForm.geshouxingbie = obj[o];
              this.ro.geshouxingbie = true;
              continue;
            }
            if(o=='nianling'){
              this.ruleForm.nianling = obj[o];
              this.ro.nianling = true;
              continue;
            }
            if(o=='xingzuo'){
              this.ruleForm.xingzuo = obj[o];
              this.ro.xingzuo = true;
              continue;
            }
            if(o=='chushengriqi'){
              this.ruleForm.chushengriqi = obj[o];
              this.ro.chushengriqi = true;
              continue;
            }
            if(o=='diqu'){
              this.ruleForm.diqu = obj[o];
              this.ro.diqu = true;
              continue;
            }
            if(o=='gerenzhuanji'){
              this.ruleForm.gerenzhuanji = obj[o];
              this.ro.gerenzhuanji = true;
              continue;
            }
            if(o=='geshoujieshao'){
              this.ruleForm.geshoujieshao = obj[o];
              this.ro.geshoujieshao = true;
              continue;
            }
            if(o=='discussnum'){
              this.ruleForm.discussnum = obj[o];
              this.ro.discussnum = true;
              continue;
            }
            if(o=='storeupnum'){
              this.ruleForm.storeupnum = obj[o];
              this.ro.storeupnum = true;
              continue;
            }
          }
        }else if(type=='edit'){
			this.info()
		}
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
        this.$http.get('option/biaoqian/biaoqian', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.biaoqianOptions = res.data.data;
          }
        });
        this.$http.get('option/zimu/zimu', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.zimuOptions = res.data.data;
          }
        });
        this.geshouxingbieOptions = "男,女".split(',')

		if (localStorage.getItem('raffleType') && localStorage.getItem('raffleType') != null) {
			localStorage.removeItem('raffleType')
			setTimeout(() => {
				this.onSubmit()
			}, 300)
		}
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`singer/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

			//更新跨表属性
			var crossuserid;
			var crossrefid;
			var crossoptnum;
			this.$refs["ruleForm"].validate(valid => {
				if(valid) {
					if(this.type=='cross'){
						var statusColumnName = localStorage.getItem('statusColumnName');
						var statusColumnValue = localStorage.getItem('statusColumnValue');
						if(statusColumnName && statusColumnName!='') {
							var obj = JSON.parse(localStorage.getItem('crossObj'));
							if(!statusColumnName.startsWith("[")) {
								for (var o in obj){
									if(o==statusColumnName){
										obj[o] = statusColumnValue;
									}
								}
								var table = localStorage.getItem('crossTable');
								this.$http.post(table+'/update', obj).then(res => {});
							} else {
								crossuserid=Number(localStorage.getItem('frontUserid'));
								crossrefid=obj['id'];
								crossoptnum=localStorage.getItem('statusColumnName');
								crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
							}
						}
					}
					if(crossrefid && crossuserid) {
						this.ruleForm.crossuserid=crossuserid;
						this.ruleForm.crossrefid=crossrefid;
						var params = {
							page: 1,
							limit: 10,
							crossuserid:crossuserid,
							crossrefid:crossrefid,
						}
						this.$http.get('singer/list', {
							params: params
						}).then(res => {
							if(res.data.data.total>=crossoptnum) {
								this.$message({
									message: localStorage.getItem('tips'),
									type: 'error',
									duration: 1500,
								});
								return false;
							} else {
								// 跨表计算


								this.$http.post(`singer/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
									if (res.data.code == 0) {
										this.$message({
											message: '操作成功',
											type: 'success',
											duration: 1500,
											onClose: () => {
												this.$router.go(-1);
											}
										});
									} else {
										this.$message({
											message: res.data.msg,
											type: 'error',
											duration: 1500
										});
									}
								});
							}
						});
					} else {


						this.$http.post(`singer/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
							if (res.data.code == 0) {
								this.$message({
									message: '操作成功',
									type: 'success',
									duration: 1500,
									onClose: () => {
										this.$router.go(-1);
									}
								});
							} else {
								this.$message({
									message: res.data.msg,
									type: 'error',
									duration: 1500
								});
							}
						});
					}
				}
			});
		},
		// 获取uuid
		getUUID () {
			return new Date().getTime();
		},
		// 返回
		back() {
			this.$router.go(-1);
		},
      touxiangUploadChange(fileUrls) {
          this.ruleForm.touxiang = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");
      },
    }
  };
</script>

